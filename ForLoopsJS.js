
//Classical For Loop
var n=3;
for(var i=0; i<n; i++)
    console.log("Num: "+ i);

//Rare Modification of For loop
//For Each
let fruits=['apple','orange','peach'];
console.log("FOR EACH LOOP");
fruits.forEach(it=> console.log("Items of Fruits: "+ it));

//For of -- this are for the iterables i.e; Arrays & Strings, Arraylist
let fruity = ['Apple','Peach','Orange'];
console.log("FOR OF LOOP");
for(item of fruity){
    console.log("Items of Fruity : "+ item);
}

//For in -- This returns the emurables i.e; Objects
//it can also be used for iterables in which index acts as KEY
let Fruitss = ['Fig','pumpkin','Nuts'];
console.log("FOR IN LOOP");
for(item in Fruitss){
            console.log("Index of Fruit Items: "+ item);
}